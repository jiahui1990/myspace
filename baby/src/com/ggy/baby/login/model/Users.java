package com.ggy.baby.login.model;

import javacommon.base.model.GlobalVariable;

/**
 * 用户对象
 * @author Chencong
 *
 */
public class Users {
	/**
	 * 用户id
	 */
	private Long id;	
	/**
	 * 用户名
	 */
	private String username;
	/**
	 * 密码
	 */
	private String password;
	/**
	 * 昵称
	 */
	private String nickName;
	/**
	 * 手机号码
	 */
	private String telphone;
	/**
	 * 地址
	 */
	private String address;
	/**
	 * 生效时间
	 */
	private String operationTime;
	/**
	 * 过期时间
	 */
	private String expireTime;
	/**
	 * 创建时间
	 */
	private String createTime;
	/**
	 * 是否被保存了
	 */
	private GlobalVariable isSaved;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getTelphone() {
		return telphone;
	}
	public void setTelphone(String telphone) {
		this.telphone = telphone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	
	
	
	public Users(Long id, String username, String password, String nickName,
			String telphone, String address, String operationTime,
			String expireTime, String createTime) {
		super();
		this.id = id;
		this.username = username;
		this.password = password;
		this.nickName = nickName;
		this.telphone = telphone;
		this.address = address;
		this.operationTime = operationTime;
		this.expireTime = expireTime;
		this.createTime = createTime;
	}
	
	public Users(Long id) {
		super();
		this.id = id;
	}
	public Users() {
		super();
	}
	public String getExpireTime() {
		return expireTime;
	}
	public void setExpireTime(String expireTime) {
		this.expireTime = expireTime;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getOperationTime() {
		return operationTime;
	}
	public void setOperationTime(String operationTime) {
		this.operationTime = operationTime;
	}
	public GlobalVariable getIsSaved() {
		return isSaved;
	}
	public void setIsSaved(GlobalVariable isSaved) {
		this.isSaved = isSaved;
	}
	
	
}
