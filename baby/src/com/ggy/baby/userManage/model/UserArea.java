package com.ggy.baby.userManage.model;

import com.ggy.baby.login.model.Users;
import com.ggy.baby.monitorManage.model.KindergartenArea;

/**
 * 用户区域中间表
 * @author Chencong
 *
 */
public class UserArea {
	/**
	 * 用户区域id
	 */
	private Long id;
	/**
	 * 用户id
	 */
	private Users userId;
	/**
	 * 区域id
	 */
	private KindergartenArea areaId;
	/**
	 * 创建事件
	 */
	private String createTime;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Users getUserId() {
		return userId;
	}
	public void setUserId(Users userId) {
		this.userId = userId;
	}
	public KindergartenArea getAreaId() {
		return areaId;
	}
	public void setAreaId(KindergartenArea areaId) {
		this.areaId = areaId;
	}
	public UserArea(Long id, Users userId, KindergartenArea areaId) {
		super();
		this.id = id;
		this.userId = userId;
		this.areaId = areaId;
	}
	public UserArea() {
		super();
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
}
