package javacommon.base.model;

import java.util.Date;


/**
 * Globalvariable entity. @author MyEclipse Persistence Tools
 */

public class GlobalVariable extends BaseModel implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6281738260466484501L;
	/**
	 * 主键id
	 */
	private Long id;
	/**
	 * 全局变量编号
	 */
	private String numbers;
	/**
	 * 全局变量类容
	 */
	private String content;
	/**
	 * 全局变量备注
	 */
	private String comments;
	/**
	 * 创建时间
	 */
	private String createtime;
	

	// Constructors

	/** default constructor */
	public GlobalVariable() {}
	public GlobalVariable(Long id) {
		this.id=id;
	}
	
	public GlobalVariable(String numbers) {
		this.numbers = numbers;
	}

	/** full constructor */
	public GlobalVariable(Long id, String numbers, String content, String comments, String createtime) {
		this.id = id;
		this.numbers = numbers;
		this.content = content;
		this.comments = comments;
		this.createtime = createtime;
	}

	// Property accessors

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNumbers() {
		return this.numbers;
	}

	public void setNumbers(String numbers) {
		this.numbers = numbers;
	}

	public String getContent() {
		return this.content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	public String getComments() {
		return comments;
	}


	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

}